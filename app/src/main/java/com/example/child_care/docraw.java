package com.example.child_care;
public class docraw {

    private String name,phone,spe,image;

    public docraw(String name, String phone, String spe, String image) {
        this.name = name;
        this.phone = phone;
        this.spe = spe;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getSpe() {
        return spe;
    }

    public void setSpe(String spe) {
        this.spe = spe;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public docraw()
    {


    }
}
